int		ft_strcmp(char *s1, char *s2)
{
	int n;

	n = 0;
	while (s1[n] == s2[n] && s1[n] != '\0' && s2[n] != '\0')
	{
		n++;
	}
	if (s1[n] == s2[n])
	{
		return (0);
	}
	else if (s1[n] < 0 && s2[n] < 0)
	{
		return (s1[n] - s2[n]);
	}
	else if (s2[n] < 0)
	{
		return (s1[n] - s2[n] - 256);
	}
	else if (s1[n] < 0)
	{
		return (s1[n] - s2[n] + 256);
	}
	return (s1[n] - s2[n]);
}
